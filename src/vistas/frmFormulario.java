    private void myInitComponents() {
        rdbTiempo.setText(vg.getPOR_TIEMPO());
        rdbEjercicios.setText(vg.getPOR_EJERCICIOS());
        lblTiempo.setText(vg.getTIEMPO());
        lblMinutos.setText(vg.getMINUTOS());
        lblSegundos.setText(vg.getSEGUNDOS());
        lblCantidadEjercicios.setText(vg.getCANTIDAD_EJERCICIOS());
        lblDigitos.setText(vg.getDIGITOS());
        lblCifras.setText(vg.getCIFRAS());
        btnSiguiente.setText(vg.getCOMENZAR());
        
        switch(this.opcion) {
            case 1: // Suma
                lblNombreEjercicios.setText(vg.getSUMA());
                break;
            case 2: // Resta
                lblNombreEjercicios.setText(vg.getRESTA());
                break;
            case 3: // Multiplicacion
                lblNombreEjercicios.setText(vg.getMULTIPLICACION());
               break;
            case 4: // Division
                lblNombreEjercicios.setText(vg.getDIVISION());
                break;
            case 5: // Porcentaje
                lblNombreEjercicios.setText(vg.getPORCENTAGE());
                break;
        }
        this.setTitle(lblNombreEjercicios.getText());
    }
    
    private void porTiempo(boolean porTiempo) {
        lblCantidadEjercicios.setVisible(!porTiempo);
        txtCantidadEjercicios.setVisible(!porTiempo);
        
        lblTiempo.setVisible(porTiempo);
        txtMinutos.setVisible(porTiempo);
        lblMinutos.setVisible(porTiempo);
        txtSegundos.setVisible(porTiempo);
        lblSegundos.setVisible(porTiempo);
        
        rdbTiempo.setSelected(porTiempo);
        rdbEjercicios.setSelected(!porTiempo);
        
        
        txtCantidadEjercicios.setText("0");
        txtMinutos.setText("0");
        txtSegundos.setText("0");
    }
    
    public void ocultarCifras() {
        lblCifras.setVisible(false);
        spnCifras.setVisible(false);
        spnCifras.setValue(0);
    }

